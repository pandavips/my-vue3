/**
 * 采用二进制位运算来优化性能
 * 赋值使用 |
 * 查询判断使用 &
 */
export const enum ShapeFlags {
  // 原生element
  ELEMENT = 1, // 0001
  // vue组件
  STATEFUL_COMPONENT = 1 << 1, // 0010
  // 文本子节点
  TEXT_CHILDREN = 1 << 2, // 0100
  // 数组类型的子节点
  ARRAY_CHILDREN = 1 << 3, // 1000
  // slots
  SLOT_CHILDREN = 1 << 4,
}

// 巧妙地采用了位运算,直接赋值修改或者查找,不够高效,所以我们使用位运算符

/**
 * | &
 * 修改的话我们用 |
 *  0001 > 0000 >> 0001|0000 >>> 0001
 * 查询的话我们用 &
 *  0001 > 0000 >> 0001&0000 >>> 0000
 */
